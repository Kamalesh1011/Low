@router.post("/repos/push")
async def push_code_to_repo(body: PushCodeRequest):
    """Push generated code files to a GitHub repository."""
    try:
        # Get the authenticated user info
        user_info = await github_get("/user", body.github_token)
        username = user_info["login"]
        repo_full = f"{username}/{body.repo_name}"
        
        # Check if repo exists, create if not
        try:
            await github_get(f"/repos/{repo_full}", body.github_token)
            logger.info("github.repo_exists", repo=repo_full)
        except HTTPException:
            # Create the repo
            await github_post("/user/repos", body.github_token, {
                "name": body.repo_name,
                "description": body.description,
                "private": body.private,
                "auto_init": True,
            })
            # Small delay for repo to be ready
            import asyncio
            await asyncio.sleep(2)
        
        # Push each file
        pushed_files = []
        failed_files = []
        
        for filename, content in body.files.items():
            try:
                # Check if file exists to get SHA (needed for updates)
                sha = None
                try:
                    existing = await github_get(
                        f"/repos/{repo_full}/contents/{filename}",
                        body.github_token
                    )
                    sha = existing.get("sha")
                except HTTPException:
                    pass  # File doesn't exist yet, that's ok
                
                # Encode content to base64
                content_b64 = base64.b64encode(content.encode()).decode()
                
                payload = {
                    "message": f"🤖 Add {filename} - Generated by OmniForge Nexus",
                    "content": content_b64,
                    "branch": "main",
                }
                if sha:
                    payload["sha"] = sha
                
                await github_put(
                    f"/repos/{repo_full}/contents/{filename}",
                    body.github_token,
                    payload
                )
                pushed_files.append(filename)
            
            except Exception as fe:
                logger.warning("github.file_push_failed", file=filename, error=str(fe))
                failed_files.append(filename)
        
        return {
            "success": True,
            "repo_url": f"https://github.com/{repo_full}",
            "pushed_count": len(pushed_files),
            "failed_count": len(failed_files),
            "pushed_files": pushed_files,
            "failed_files": failed_files,
            "branch": "main",
        }
    
    except HTTPException:
        raise
    except Exception as e:
        logger.error("github.push_failed", error=str(e))
        raise HTTPException(status_code=500, detail=str(e))


@router.post("/deploy/pages")
async def deploy_to_github_pages(body: DeployRequest):
    """Enable GitHub Pages for a repository (for static website deployment)."""
    try:
        user_info = await github_get("/user", body.github_token)
        username = user_info["login"]
        repo_full = f"{username}/{body.repo_name}"
        
        # Enable GitHub Pages
        async with httpx.AsyncClient() as client:
            resp = await client.post(
                f"{GITHUB_API}/repos/{repo_full}/pages",
                headers={
                    "Authorization": f"token {body.github_token}",
                    "Accept": "application/vnd.github.v3+json",
                },
                json={
                    "source": {
                        "branch": body.branch,
                        "path": "/"
                    }
                },
                timeout=30,
            )
            
            if resp.status_code in [201, 409]:  # 409 = already enabled
                pages_url = f"https://{username}.github.io/{body.repo_name}"
                return {
                    "success": True,
                    "pages_url": pages_url,
                    "repo_url": f"https://github.com/{repo_full}",
                    "message": "GitHub Pages deployment initiated",
                }
            else:
                resp.raise_for_status()
    
    except HTTPException:
        raise
    except Exception as e:
        logger.error("github.pages_deploy_failed", error=str(e))
        raise HTTPException(status_code=500, detail=str(e))


