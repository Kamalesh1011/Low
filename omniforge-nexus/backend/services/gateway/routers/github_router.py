"""
OmniForge Nexus – GitHub Integration Router
Handles GitHub OAuth, repo creation, code push, and GitHub Pages/Actions deployment.
"""
import base64
import json
from typing import Optional
from fastapi import APIRouter, HTTPException, Request
from fastapi.responses import RedirectResponse
from pydantic import BaseModel
import httpx
import structlog

from shared.config import settings

router = APIRouter()
logger = structlog.get_logger(__name__)

GITHUB_API = "https://api.github.com"


# ── Request/Response Models ──────────────────────────────────────

class GitHubTokenRequest(BaseModel):
    token: str  # Personal Access Token


class PushCodeRequest(BaseModel):
    github_token: str
    repo_name: str
    files: dict[str, str]  # filename → content
    description: Optional[str] = "Generated by OmniForge Nexus"
    private: bool = False


class CreateRepoRequest(BaseModel):
    github_token: str
    repo_name: str
    description: Optional[str] = "Generated by OmniForge Nexus"
    private: bool = False
    auto_init: bool = True


class DeployRequest(BaseModel):
    github_token: str
    repo_name: str
    branch: str = "main"


# ── Helpers ──────────────────────────────────────────────────────

async def github_get(endpoint: str, token: str) -> dict:
    """Make an authenticated GET request to GitHub API."""
    async with httpx.AsyncClient() as client:
        resp = await client.get(
            f"{GITHUB_API}{endpoint}",
            headers={
                "Authorization": f"token {token}",
                "Accept": "application/vnd.github.v3+json",
                "X-GitHub-Api-Version": "2022-11-28",
            },
            timeout=30,
        )
        if resp.status_code == 401:
            raise HTTPException(status_code=401, detail="Invalid GitHub token")
        if resp.status_code == 404:
            raise HTTPException(status_code=404, detail="GitHub resource not found")
        resp.raise_for_status()
        return resp.json()


async def github_post(endpoint: str, token: str, payload: dict) -> dict:
    """Make an authenticated POST request to GitHub API."""
    async with httpx.AsyncClient() as client:
        resp = await client.post(
            f"{GITHUB_API}{endpoint}",
            headers={
                "Authorization": f"token {token}",
                "Accept": "application/vnd.github.v3+json",
                "X-GitHub-Api-Version": "2022-11-28",
                "Content-Type": "application/json",
            },
            json=payload,
            timeout=30,
        )
        if resp.status_code == 401:
            raise HTTPException(status_code=401, detail="Invalid GitHub token")
        if resp.status_code == 422:
            detail = resp.json().get("message", "Validation failed")
            raise HTTPException(status_code=422, detail=detail)
        resp.raise_for_status()
        return resp.json()


async def github_put(endpoint: str, token: str, payload: dict) -> dict:
    """Make an authenticated PUT request to GitHub API."""
    async with httpx.AsyncClient() as client:
        resp = await client.put(
            f"{GITHUB_API}{endpoint}",
            headers={
                "Authorization": f"token {token}",
                "Accept": "application/vnd.github.v3+json",
                "X-GitHub-Api-Version": "2022-11-28",
                "Content-Type": "application/json",
            },
            json=payload,
            timeout=30,
        )
        if resp.status_code == 401:
            raise HTTPException(status_code=401, detail="Invalid GitHub token")
        resp.raise_for_status()
        return resp.json()


