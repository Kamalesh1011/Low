# ── Request/Response Models ──────────────────────────────────────

class GitHubTokenRequest(BaseModel):
    token: str  # Personal Access Token


class PushCodeRequest(BaseModel):
    github_token: str
    username: Optional[str] = None
    repo_name: str
    files: dict[str, str]  # filename → content
