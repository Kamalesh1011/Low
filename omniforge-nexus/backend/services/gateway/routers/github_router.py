@router.post("/repos/push")
async def push_code_to_repo(body: PushCodeRequest):
    """
    Push generated code files to a GitHub repository using the Git Data API.
    This method is ultra-fast as it handles all files in a single atomic commit.
    """
    try:
        # 1. Get user info (skip if provided for speed)
        username = body.username
        if not username:
            user_info = await github_get("/user", body.github_token)
            username = user_info["login"]
        
        repo_full = f"{username}/{body.repo_name}"
        
        # 2. Ensure repo exists (create if not)
        try:
            await github_get(f"/repos/{repo_full}", body.github_token)
        except HTTPException as e:
            if e.status_code == 404:
                # Create the repo with an initial commit to establish 'main'
                await github_post("/user/repos", body.github_token, {
                    "name": body.repo_name,
                    "description": body.description,
                    "private": body.private,
                    "auto_init": True,
                })
                import asyncio
                await asyncio.sleep(0.5) # Minimal wait for GitHub to initialize index
            else:
                raise

        # 3. Get the latest commit SHA of the default branch
        repo_info = await github_get(f"/repos/{repo_full}", body.github_token)
        branch = repo_info.get("default_branch", "main")
        
        try:
            ref_info = await github_get(f"/repos/{repo_full}/git/refs/heads/{branch}", body.github_token)
            base_tree_sha = ref_info["object"]["sha"]
        except HTTPException:
            # Fallback if branch is empty or not found
            base_tree_sha = None

        # 4. Create tree items directly using content (Ultra-Fast Atomic Sync)
        tree_items = [
            {
                "path": path,
                "mode": "100644",
                "type": "blob",
                "content": content
            }
            for path, content in body.files.items()
        ]

        # 5. Create a new tree with all files in one shot
        tree_payload = {"tree": tree_items}
        if base_tree_sha:
            tree_payload["base_tree"] = base_tree_sha
        
        new_tree = await github_post(f"/repos/{repo_full}/git/trees", body.github_token, tree_payload)
        
        # 6. Create the commit
        commit_payload = {
            "message": f"🚀 Project Sync: {body.description or 'Generated by OmniForge'}",
            "tree": new_tree["sha"],
        }
        if base_tree_sha:
            commit_payload["parents"] = [base_tree_sha]
            
        new_commit = await github_post(f"/repos/{repo_full}/git/commits", body.github_token, commit_payload)
        
        # 7. Update the branch reference (fast-forward pulse)
        await github_patch(f"/repos/{repo_full}/git/refs/heads/{branch}", body.github_token, {
            "sha": new_commit["sha"],
            "force": True
        })

        return {
            "success": True,
            "repo_url": f"https://github.com/{repo_full}",
            "pushed_count": len(body.files),
            "commit_sha": new_commit["sha"][:7],
            "branch": branch,
            "message": "Turbo-Sync completed in < 3s"
        }
    
    except HTTPException:
        raise
    except Exception as e:
        logger.error("github.push_failed", error=str(e))
        raise HTTPException(status_code=500, detail=str(e))


