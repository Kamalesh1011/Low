class DeployRequest(BaseModel):
    github_token: str
    repo_name: str
    branch: str = "main"


# ── Helpers ──────────────────────────────────────────────────────

async def github_get(endpoint: str, token: str) -> dict:
    """Make an authenticated GET request to GitHub API."""
    async with httpx.AsyncClient() as client:
        resp = await client.get(
            f"{GITHUB_API}{endpoint}",
            headers={
                "Authorization": f"token {token}",
                "Accept": "application/vnd.github.v3+json",
                "X-GitHub-Api-Version": "2022-11-28",
            },
            timeout=30,
        )
        if resp.status_code == 401:
            raise HTTPException(status_code=401, detail="Invalid GitHub token")
        if resp.status_code == 404:
            raise HTTPException(status_code=404, detail="GitHub resource not found")
        resp.raise_for_status()
        return resp.json()


async def github_post(endpoint: str, token: str, payload: dict) -> dict:
    """Make an authenticated POST request to GitHub API."""
    async with httpx.AsyncClient() as client:
        resp = await client.post(
            f"{GITHUB_API}{endpoint}",
            headers={
                "Authorization": f"token {token}",
                "Accept": "application/vnd.github.v3+json",
                "X-GitHub-Api-Version": "2022-11-28",
                "Content-Type": "application/json",
            },
            json=payload,
            timeout=30,
        )
        if resp.status_code == 401:
            raise HTTPException(status_code=401, detail="Invalid GitHub token")
        if resp.status_code == 422:
            detail = resp.json().get("message", "Validation failed")
            raise HTTPException(status_code=422, detail=detail)
        resp.raise_for_status()
        return resp.json()


